import { useLocation } from "react-router-dom";

export default function IconsPage() {
  const navigate = useNavigate();
  const [selectedIcon, setSelectedIcon] = useState(null);
  const location = useLocation();

  // Get selected category from URL
  const params = new URLSearchParams(location.search);
  const selectedCategory = params.get("category");

  // Determine which icons to show
  let iconList = allIconNames;
  if (selectedIcon) {
    iconList = [selectedIcon];
  } else if (selectedCategory && iconCategories[selectedCategory]) {
    iconList = iconCategories[selectedCategory];
  }
  // Filter to only valid icon components (functions or React.forwardRef objects)
  iconList = iconList.filter((name) => {
    const icon = Icons[name];
    const isValid =
      typeof icon === "function" ||
      (typeof icon === "object" && icon !== null && "$$typeof" in icon);
    if (!isValid) {
      console.warn(`Skipping invalid icon: ${name}`, icon);
    }
    return isValid;
  });


  return (
    <div className="bg-gray-100 dark:bg-gray-900 min-h-screen flex flex-col">
      {/* Main Content */}
      <div>
        <h2 className="font-bold text-xl mb-6">
          {selectedIcon
            ? selectedIcon
            : selectedCategory
            ? `${selectedCategory} Icons`
            : "All Icons"}
        </h2>
        <div className="grid grid-cols-2 sm:grid-cols-3 md:grid-cols-4 lg:grid-cols-6 gap-6">
          {iconList.map((name) => {
            const LucideIcon = Icons[name];
            return (
              <div
                key={name}
                className="flex flex-col items-center p-3 bg-white dark:bg-gray-800 rounded shadow hover:shadow-lg transition"
              >
                <LucideIcon size={32} />
                <span className="mt-2 text-xs text-center text-gray-700 dark:text-gray-200 break-all">
                  {name}
                </span>
              </div>
            );
          })}
        </div>
      </div>
    </div>
  );
}
